#print默认是打印一行，结尾加换行。end=' '意思是末属尾不换行，加空格。

print('a',end='') #不换行
print('b',end='123\n')  #换行 ab123

chinese="零一二三四五六七八九"
number = input("输入数字")

for i in number:
    print(chinese[int(i)],end="")
    