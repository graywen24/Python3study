import time

scale = 10
print("start".center(20,"-"))   #-------kaishi-------
for i in range(scale + 1):
    #print(i)
    a = "*"*i
    b = '.'*(scale - i)
    c = (i/scale)*100
    print("{:=^5.0f}%[{}-->{}]".format(c,a,b))   # = fill in space ^-center 3- length .0f - 0位浮点
    #100%[**********-->.........]   [--->]    print directly 
    time.sleep(0.1)
print("end".center(20,"-"))
