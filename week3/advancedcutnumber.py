#[M:N]M 确实表示从开头 N确实表示到结尾

a = "0123456789"[:3]
print("0123456789"[:3])   #012
print("0123456789"[3:]) #3456789


#[M:N:K ]  K 为步长度
print("0123456789"[1::2]) #1357 第一个到最后一个 隔2个去一个
print("0123456789"[0:8:1]) #01234567 取第0个到第八个
print("0123456789"[::-1])  #9876543210 字符串逆

